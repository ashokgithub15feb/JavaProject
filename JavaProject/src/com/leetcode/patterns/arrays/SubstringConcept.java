package com.leetcode.patterns.arrays;

public class SubstringConcept {

	public static void main(String[] args) {
		          //012345678
		String s = "hamburger";
		
		s = s.substring(4, 8);
		System.out.println(s);
		s = s.substring(1);
		System.out.println(s);
		s = s.substring(1);
		System.out.println(s);
	}
}
